/* 5. Mencari angka terbesar dari nilai berikut 4, 8, 2
Jangan menggunakan metode dari javascript
tapi menggunakan "If Else Statement"
Untuk mengecek mana bilangan yg terbesar dari nilai yg telah ditentukan */

var bil1 = 4,
		bil2 = 8,
		bil3 = 2

var nilaiTerbesar

if(bil1 > bil2) {
	nilaiTerbesar = bil1
} else {
	nilaiTerbesar = bil2
}

if (nilaiTerbesar > bil3) {
	nilaiTerbesar = nilaiTerbesar
} else {
	nilaiTerbesar = bil3
}

console.log("Nilai terbesar dari 4, 8 dan 2 adalah " +nilaiTerbesar)

